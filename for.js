// for (inicialización, condición, incremento/decremento)

let x = 0
for (x; x<50; x++){
 console.log("El valor de x es: "+ x) // sentencia que se repetirá mientras la variable X cumpla la condición
}


let y = 50;
for (y; y>0 ; y--) {
  console.log("El valor de y es: " + y); // sentencia que se repetirá mientras la variable y cumpla la condición
}

